<h1>SAVES LIFE</h1>
<b>
Saves Life is an initiative that began with a thought so that no one has to lose their loved ones due to unavailability of blood. We connect patients in need to donors willing to help via blood banks.<br>
We use Blood Bank Management System that is a simple PHP/MySQL project that manages blood inventory in a certain blood bank. The system only allows the blood bank's management to access the data because the system focuses only on the data management or the inventory of the blood availability in a certain blood bank. This system stores the list of donors, blood donations, requests and hands over requested blood.<br> The blood donations list in this system will serve as the receiving or the process where we can update the volume availability of blood in each blood group. The requested feature in this system will serve as the record of requested blood for a certain patient, which is requesting for specific blood with a specific volume data will be submitted ord inputted in this process.<br> The handed over list are the records of handed over blood requests, which means this process updates the available volume of each blood group.
