<h2><u>Electrolyte Count</u></h2>
<ol>
<b>
<li>Pulse rate - Between <u> 50 and 100 </u> without irregularities.</li>
<li>Hemoglobin Level - A minimum of <u>12.5 g/dL</u>.</li>
<li>Blood pressure - <u> Diastolic</u> <b>-50-100mm Hg, <u> Systolic</u>-100-80mm Hg.</li>
<li>Body Tempreture - Should be normal,with an oral tempreture not exceeding <u> 37.5degree Celsius. </u></li>
</b>
</ol>
<h3><u>Electrolyte Requirment</u></h3>
<ul>
<li>Overall health - The donar must be <u>fit and healthy </u>, and should </u>not be suffering from</u> transmittable diseases.</li>
<li>HIV Postive - <u>HIV Positive</u> person <u>not </u>able to donate blood.</li>
<li>Ailments - Individuals suffering from ailments like <u> cardic arrest, hypertension, blood pressure, cancer, diabetes, elilepsy are not able to donate blood. </u></li>
<li>Hepatitis B vaccine - Individuals treated for <u> rabies</u> or received Hepatities B vaccine in the past <u>6 months </u> not able to donate blood.</li>
<li>Pregnant Women - Woman who are pregnant and who had <u> miscarriage</u> in past<u> 6 months </u> are not able to donate.</li>
<li>Alcohholic - a person who has consumed alcohol in past <u> 24 hours </u> not able to donate blood.</li>
</ul>
